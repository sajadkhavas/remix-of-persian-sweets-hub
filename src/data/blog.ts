export function findPost(slug: string): BlogPost | undefined {
  return POSTS.find((p) => p.slug === slug);
}

export function getRelatedPosts(currentSlug: string, count = 2): BlogPost[] {
  return POSTS.filter((p) => p.slug !== currentSlug).slice(0, count);
}

export function formatPersianDate(dateString: string): string {
  const [year, month, day] = dateString.split("-").map(Number);
  const months = [
    "فروردین",
    "اردیبهشت",
    "خرداد",
    "تیر",
    "مرداد",
    "شهریور",
    "مهر",
    "آبان",
    "آذر",
    "دی",
    "بهمن",
    "اسفند",
  ];
  const jYear = year - 621;
  const jMonth = month <= 3 ? month + 9 : month - 3;
  const pd = (n: number) => n.toString().replace(/\d/g, (d) => "۰۱۲۳۴۵۶۷۸۹"[parseInt(d)]);
  return `${pd(day)} ${months[jMonth - 1]} ${pd(jYear)}`;
}

export function estimateReadingTime(content: string): string {
  const minutes = Math.ceil(content.split(/\s+/).length / 150);
  const pd = (n: number) => n.toString().replace(/\d/g, (d) => "۰۱۲۳۴۵۶۷۸۹"[parseInt(d)]);
  return `${pd(minutes)} دقیقه`;
}
